#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
	return self.flows.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Flow Detail Cell" forIndexPath:indexPath];
	
	NSDictionary *flow = self.flows[indexPath.row];
	cell.textLabel.text = flow[@"name"];
	cell.imageView.image = [UIImage imageNamed:flow[@"image"]];
	
    return cell;
}

